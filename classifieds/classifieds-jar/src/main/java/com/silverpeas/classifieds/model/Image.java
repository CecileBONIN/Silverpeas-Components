package com.silverpeas.classifieds.model;

import java.io.Serializable;

public class Image implements Serializable {
  private static final long serialVersionUID = -2746485759326084199L;
  private int imageId;
  private int classifiedId;
  private String imageName;
  private String mimeType;

  public Image(int classifiedId, String imageName, String mimeType) {
    this.classifiedId = classifiedId;
    this.imageName = imageName;
    this.mimeType = mimeType;
  }

  public int getImageId() {
    return imageId;
  }

  public void setImageId(int imageId) {
    this.imageId = imageId;
  }

  public int getClassifiedId() {
    return classifiedId;
  }

  public void setClassifiedId(int classifiedId) {
    this.classifiedId = classifiedId;
  }

  public String getImageName() {
    return imageName;
  }

  public void setImageName(String imageName) {
    this.imageName = imageName;
  }

  public String getMimeType() {
    return mimeType;
  }

  public void setMimeType(String mimeType) {
    this.mimeType = mimeType;
  }
}
